# ✅ 문제 1: 1부터 100까지의 합 (기초)
# 가장 기본적인 누적 합계 문제입니다.
# 코드 분석:
# i = i + 1: 0부터 시작해서 1, 2, 3... 100까지 숫자를 만듭니다. (순서도 ②번)
# j = j + i: 만든 숫자를 계속 j라는 주머니에 담습니다. (순서도 ①번)
# while i < 100: i가 100이 될 때까지 반복합니다. (마지막에 100을 더하고 나면 i가 100이 되어 조건이 False가 되므로 종료)
# 결과: 5050
# ✅ 문제 2: 스위치 변수 활용 (1 - 2 + 3 - 4 ...)
# 홀수는 더하고 짝수는 빼는 로직을 SW(스위치) 변수로 제어하는 문제입니다.
# 코드 분석 & 순서도 매칭:
# 빈칸 ① (SW = 0): 처음에 1을 더해야 하므로, 시작할 때 스위치를 0(Yes 방향)으로 맞춰둡니다.
# 빈칸 ② (i = i + 1): 숫자를 1씩 증가시킵니다.
# 빈칸 ③ (j = j - i): else 부분입니다. 스위치가 0이 아닐 때(No)는 뺄셈을 수행합니다.
# 빈칸 ④ (sw = 0): 뺄셈을 했으면 다음번엔 덧셈을 해야 하므로 스위치를 다시 0으로 돌려놓습니다.
# 칭찬 포인트:
# 순서도 그림의 Yes 방향을 보면 SW = 0이라고 잘못 나와 있는데(그려면 계속 더하기만 함), 코드에서는 sw = 1로 올바르게 수정해서 작성하셨습니다. 완벽합니다.
# ✅ 문제 3: 나머지 연산 활용 (-1 + 2 - 3 + 4 ...)
# 홀수는 빼고 짝수는 더하는 로직을 MOD(나머지 연산)로 제어하는 문제입니다.
# 코드 분석 & 순서도 매칭:
# 빈칸 ① (j = 0): 합계를 담을 변수 초기화입니다.
# 빈칸 ② (i = i + 1): 숫자 증가입니다.
# 빈칸 ③ (j = j - i): else 부분(홀수)입니다. i가 홀수일 때는 빼기를 수행합니다.
# (작성하신 주석 # 3 : j - j = i는 오타 같고, 코드는 j = j - i로 맞게 쓰셨습니다)
# 핵심 포인트:
# 순서도의 MOD(i, 2) = 0은 파이썬의 if i % 2 == 0:과 완벽하게 같습니다.
# 짝수일 때는 더하고(j = j + i), 홀수일 때는 뺍니다(j = j - i).