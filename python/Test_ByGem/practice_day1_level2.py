print("\n--- [문제 2: for문 (하나씩 꺼내기)] ---")
# 설명: 리스트에 있는 AI 모델 이름들을 하나씩 꺼내서 출력하세요.
# 출력 예시:
# 모델명: GPT-4
# 모델명: Claude-3
# 모델명: Gemini-Pro

models = ["GPT-4", "Claude-3", "Gemini-Pro"]

# [코드 작성 공간] --------------------------
for m in models:
    # 힌트: 변수 m에는 리스트 안의 내용물이 하나씩 들어옵니다.
    print(f"모델명: {m}") # 이 줄은 수정하지 마세요. 그냥 실행 흐름을 이해해보세요!
